    public final void loadData() {
    String SUrl = "jdbc:MySQL://localhost:3306/java_db";
    String SUser = "root";
    String SPass = "";
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
        Statement st = con.createStatement();
        DefaultTableModel model = new DefaultTableModel(new String[]{"No Tiket", "Masalah", "Prioritas", "Department"}, 0);
        tblMhs.setModel(model);
        String sql = "SELECT * FROM tiketbaru";
        ResultSet rs = st.executeQuery(sql);
        while (rs.next()) {
            int id = rs.getInt("id");
            String subjek = rs.getString("subjek");
            String prioritas = rs.getString("prioritas");
            String tags = rs.getString("tags");
            model.addRow(new Object[]{id, subjek, prioritas, tags});
        }
        con.close();
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
}

