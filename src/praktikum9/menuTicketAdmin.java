    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String ID = txtCari.getText();
    String SUrl = "jdbc:MySQL://localhost:3306/java_db";
    String SUser = "root";
    String SPass = "";
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
        Statement st = con.createStatement();
        String sql = "SELECT * FROM tiketbaru WHERE id=" + ID;
        ResultSet rs = st.executeQuery(sql);
        if (rs.next()) {
            subject.setText(rs.getString("subjek"));
            String prioritas = rs.getString("prioritas");
            jCheckBox1.setSelected(prioritas.equals(jCheckBox1.getText()));
            jCheckBox2.setSelected(prioritas.equals(jCheckBox2.getText()));
            jCheckBox3.setSelected(prioritas.equals(jCheckBox3.getText()));
            String tags = rs.getString("tags");
            jCheckBox4.setSelected(tags.contains(jCheckBox4.getText()));
            jCheckBox5.setSelected(tags.contains(jCheckBox5.getText()));
            jCheckBox6.setSelected(tags.contains(jCheckBox6.getText()));
            jCheckBox7.setSelected(tags.contains(jCheckBox7.getText()));
        } else {
            JOptionPane.showMessageDialog(new JFrame(), "Tiket tidak ada!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        con.close();
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String ID = txtCari.getText();
String SUrl = "jdbc:MySQL://localhost:3306/java_db";
String SUser = "root";
String SPass = "";

try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
    Statement st = con.createStatement();

    // Archive the ticket
    String archiveQuery = "INSERT INTO tiketselesai SELECT * FROM tiketbaru WHERE id=" + ID;
    int archiveRowsAffected = st.executeUpdate(archiveQuery);

    if (archiveRowsAffected > 0) {
        // Delete the ticket from the original table
        String deleteQuery = "DELETE FROM tiketbaru WHERE id=" + ID;
        int deleteRowsAffected = st.executeUpdate(deleteQuery);
        if (deleteRowsAffected > 0) {
            showMessageDialog(null, "Ticket Ini selesai yeay!");
        } else {
            showMessageDialog(null, "Ticket error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        showMessageDialog(null, "Invalid ID or Failed to Archive the Ticket", "Error", JOptionPane.ERROR_MESSAGE);
    }

    loadData();
    con.close();
} catch (Exception e) {
    System.out.println("Error: " + e.getMessage());
}
        
    }//GEN-LAST:event_btnDeleteActionPerformed

